import type { BrandTheme, ColorTokens } from './brand-theme';
import {
  getDefaultTheme,
  DEFAULT_SPACING,
  DEFAULT_RADIUS,
  DEFAULT_FONTS,
  DEFAULT_TYPOGRAPHY_SIZES,
  DEFAULT_TYPOGRAPHY_STYLES,
  DEFAULT_BUTTONS,
} from './brand-theme';

export interface ThemePreset {
  id: string;
  name: string;
  theme: BrandTheme;
  // Preview colors (4 dots like Supabase example)
  previewColors: [string, string, string, string];
}

// Helper function to create a theme with custom colors
function createTheme(
  name: string,
  lightColors: ColorTokens,
  darkColors: ColorTokens
): BrandTheme {
  return {
    name,
    colors: {
      light: lightColors,
      dark: darkColors,
    },
    spacing: { ...DEFAULT_SPACING },
    radius: { ...DEFAULT_RADIUS },
    fonts: { ...DEFAULT_FONTS },
    typographySizes: { ...DEFAULT_TYPOGRAPHY_SIZES },
    typographyStyles: { ...DEFAULT_TYPOGRAPHY_STYLES },
    buttons: { ...DEFAULT_BUTTONS },
  };
}

